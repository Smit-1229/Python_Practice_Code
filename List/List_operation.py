"""
There are many types of operation in list :
sum : add all value in the list
min : it gives the minimum value in the list
max : it gives the maximum value in the list 
len : count the number of element in the list

"""
list_1 = [10,20,30,2,55,20,80]
print("Sum = ",sum(list_1))
print("minimum value = ",min(list_1))
print("maximum value = ",max(list_1))
print("No of element : ",len(list_1))
